-- Policies.
-- profiles
create policy "Public profiles are viewable by everyone." on profiles for
select using (true);
create policy "Users can insert their own non-admin profile." on profiles for
insert with check (
        (
            select auth.uid()
        ) = id
        and not is_admin
    );
create policy "Users can update own profile, except for admin status." on public.profiles for
update using (
        (
            select auth.uid()
        ) = id
    ) with check (
        (
            select auth.uid()
        ) = id
        and is_admin = (
            select p.is_admin
            from public.profiles p
            where p.id = (
                    select auth.uid()
                )
        )
    );
-- profile_stats
create policy "Profile stats are viewable by everyone." on public.profile_stats for
select using (true);
create policy "Service role can manage profile stats." on public.profile_stats for all to service_role using (true) with check (true);
-- beatmaps
create policy "Beatmaps are viewable by everyone." on beatmaps for
select using (true);
create policy "Users can upload their own unranked beatmaps." on beatmaps for
insert with check (
        (
            select auth.uid()
        ) = mapper_id
        and status = 'UNRANKED'
    );
create policy "Users can update their own unranked beatmaps." on beatmaps for
update using (
        (
            select auth.uid()
        ) = mapper_id
        and status = 'UNRANKED'
    );
-- beatmap_tags
create policy "Beatmap tags are viewable by everyone." on public.beatmap_tags for
select using (true);
create policy "Mappers can insert tags for their maps." on public.beatmap_tags for
insert with check (
        exists (
            select 1
            from public.beatmaps b
            where b.id = beatmap_tags.beatmap_id
                and b.mapper_id = (
                    select auth.uid()
                )
                and b.status = 'UNRANKED'
        )
    );
create policy "Mappers can update tags for their maps." on public.beatmap_tags for
update using (
        exists (
            select 1
            from public.beatmaps b
            where b.id = beatmap_tags.beatmap_id
                and b.mapper_id = (
                    select auth.uid()
                )
                and b.status = 'UNRANKED'
        )
    ) with check (
        exists (
            select 1
            from public.beatmaps b
            where b.id = beatmap_tags.beatmap_id
                and b.mapper_id = (
                    select auth.uid()
                )
                and b.status = 'UNRANKED'
        )
    );
create policy "Mappers can delete tags for their maps." on public.beatmap_tags for delete using (
    exists (
        select 1
        from public.beatmaps b
        where b.id = beatmap_tags.beatmap_id
            and b.mapper_id = (
                select auth.uid()
            )
            and b.status = 'UNRANKED'
    )
);
-- beatmap_scores
create policy "Beatmap scores are viewable by everyone." on public.beatmap_scores for
select using (true);
create policy "Service role can submit scores." on public.beatmap_scores for
insert to service_role with check (true);
-- comments
create policy "Comments are viewable by everyone." on public.comments for
select using (true);
create policy "Users can post their own comments." on public.comments for
insert with check (
        (
            select auth.uid()
        ) = profile_id
        and not exists (
            select 1
            from public.profiles
            where id = (
                    select auth.uid()
                )
                and (
                    muted_until is not null
                    and muted_until > now()
                )
        )
    );
create policy "Users can update their own comments." on public.comments for
update using (
        (
            select auth.uid()
        ) = profile_id
        and not exists (
            select 1
            from public.profiles
            where id = (
                    select auth.uid()
                )
                and (
                    muted_until is not null
                    and muted_until > now()
                )
        )
    );
create policy "Users can delete their own comments, admins delete any." on public.comments for delete using (
    (
        select auth.uid()
    ) = profile_id
    or exists (
        select 1
        from public.profiles
        where id = (
                select auth.uid()
            )
            and is_admin = true
    )
);
